package oop;

public class Cars {
    private String make;
    private int yearsModel;

    // Setter
    public void setMake(String ma) {
        make = ma;
    }

    public void setYearsModel(int yM) {
        yearsModel = yM;
    }

    // getter
    public String getMake() {
        return make;
    }

    public int getYearsModel() {
        return yearsModel;
    }
}
