package oop;

/**
 * Static Class
 * Calc
 */
public class Calc {

    public static double add(double a, double b) {
        return a + b;
    }

    public static double Sub(double a, double b) {
        return a - b;
    }

    public static double Multiply(double a, double b) {
        return a * b;
    }

}